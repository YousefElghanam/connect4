#include <stdint.h>

uint64_t	next_power_of_two(uint64_t n)
{
	if (n == 0)
		return (1);
	if ((n & (n - 1)) == 0)
		n++;
	n--;
	n |= n >> 1;
	n |= n >> 2;
	n |= n >> 4;
	n |= n >> 8;
	n |= n >> 16;
	n |= n >> 32;
	return (n + 1);
}
